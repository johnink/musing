
<div id="what_youll_need">
	<h2>What You'll Need:</h2>
		<p>A clock, a friend, or the widget.</p>
	
</div>

<div id="long_desc">
	<h2>Basic Idea</h2>

		<p>In this game you play the part of the television, doing a one-man show while an outside influence controls the remote. Start by writing, acting, or storyboarding a scene from the middle of a sitcom, crime drama, commercial, or whatever else might be on your television.</p>

		<p>When the switcher changes your channels, abandon the scene that you’re on and start anew with a new channel.</p>

</div>

<div id="variations">
	<h2>Variations</h2>

		<p>Try thinking about it more like a time machine, jumping to different scenes in a story you’re trying to write.</p>

		<p>If the switcher is a person, they can change the channels whenever they get bored, by chiming a bell, clapping, or simply calling out what type of show they want to see next. This will give you direct feedback on what you’re doing.</p>

		<p>Using a clock, just set yourself a time between switches.</p>

		<p>The widget will give you a new prompt for every new scene at an interval you specify. Set the varied checkbox to make it switch randomly within the max interval you set. This widget can also be ran in the background while you work, so you can look over at it whenever you need inspiration.</p>

</div>

