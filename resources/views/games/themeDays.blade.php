	<h2>What You'll Need:</h2>
	<p>A calendar</p>
</div>

<div id="long_desc">
	<h2>Basic Idea</h2>
	<p>Knowing a lot of different topics is vital to making the connections you need to come up with original ideas. For instance, if you don't know about the Dagon, Huey Lewis, or computer programming, you can make any allusions or metaphors about those topics. The less you know, the fewer options you have. If you're not naturally studious, this can be a challenge.</p>

	<p>"Theme Days" is an exercise to get you curious. It will help you grasp more pieces so you can put them together in interesting ways.</p>

	<a href = "https://pixabay.com/en/pieces-of-the-puzzle-mix-hands-592798/" target="_blank"><img src="/games/themeDays/puzzle.jpg" class="game_img" alt="puzzle"/></a>

	<p>Every morning, make it a part of your routine to pick a prompt to serve as the theme for that day. Good prompts to consider include references made in books and songs you didn't understand, names of public figures you've heard but know nothing about, or just use random <a href = "http://omusing.com/game/prompter" target="_blank">prompts</a>.</p>

	<p>On a calendar, perhaps the one in your phone, write down the theme for the current date. Then, whenever you have some downtime, do a little research on your topic. You live in an age where this information is available to you almost anywhere, try to take advantage of it. Read the <a href="http://www.wikipedia.org" target="_blank">Wikipedia</a> article on your phone, then ask yourself questions and see if you can find the answers. Do a <a href="http://www.youtube.com" target="_blank">Youtube</a> search and see what comes up. Do this for a number of days, and you'll be surprised where you end up finding inspiration.</p>

</div>

<div id="variations">
	<h2>Variations</h2>
	<p>You can take this as far as you're able or willing. If you want, try to get creative with it. Draw a detailed scene or write a ficionalized account of something you've read. If you feel you're too busy to get as deep as you want to go into a topic, try instead to make it a week long theme.</p>

