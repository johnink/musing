
<div id="long_desc">
	<h2>Basic Idea</h2>
	<p>What do you say we make some music?</p>

	<p>Pick a person, picture, or memory as a prompt. Now imagine you've been tasked with providing the soundtrack to a movie based on what you chose.</p>

</div>

<div id="variations">
	<h2>Variations</h2>
	<p>This game may work better if you find a prompt that you already have some emotional attachment to. Think about someone in your life that you respect (or hate), for instance.</p>

	<p>You can also play this game just humming quietly to yourself on the bus or subway. Pick someone who seems interesting and write their soundtrack.</p>

	<p>Consider changing the genres of the film. Reimagine the subject as tragic, heroic, happy, or sad.</p>

</div>

